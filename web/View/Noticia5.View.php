                                                                            <p>O feriado está chegando e, enquanto vários de vocês já marcaram uma viagem há muito tempo, alguns estão procurando promoções de viagens de último minuto.   E, nesse momento, decidir se o cachorro vai junto ou fica em casa é causa de bastante estresse para vários donos.</p>
                                                                            <p>Você acha que o seu pet merece uma viagem?  Ou acredita que seja melhor deixá-lo em casa?  Nós analisamos as duas opções</p>
                                                                            <h4>Por que manter o seu pet em casa durante as férias: </h4>
                                                                            <ul>
                                                                                <li>Muitos animais podem achar essa mudança de rotina, ambiente e a viagem estressante.  Portanto, pode ser melhor deixá-los no cuidado de uma pessoa responsável enquanto você viaja.</li>
                                                                                <li>Pode ser difícil encontrar um destino que oferecerá tudo o que o seu pet precisa e isso limita as suas opções de destinos potenciais.</li>
                                                                                <li>Algumas companhias aéreas não aceitam pets.</li>
                                                                                <li>Qualquer danos que o seu animal causar no hotel pode gerar multas altas para você.</li>
                                                                                <li>Existem várias creches e hotéis para animais que podem cuidar do seu pet enquanto você viaja.  Consulte os seus amigos e veterinário sobre a melhor opção para o seu pet.</li>
                                                                                <li>Se você não quiser deixar o seu pet em um hotel, existe a opção de pet sitter, em que um profissional visita a sua casa todos os dias e cuida do seu animal.</li>
                                                                                <li>Alternativamente, o seu pet também pode ficar na casa de um amigo ou familiar.  É uma boa ideia levá-los para a casa em que eles vão ficar antes da viagem para eles já conhecerem e se acostumarem com esse novo ambiente.</li>
                                                                            </ul>
                                                                            <h4>Por que levar o seu pet junto na viagem:</h4>
                                                                            <ul>
                                                                                <li>Apesar do conceito de que gatos ficam bem sozinhos, não é uma boa ideia deixá-los só por um longo período de tempo.  Os gatos podem ficar doentes e, quando deixados por longos períodos de tempo sozinhos, não terão acesso aos cuidados veterinários que precisam e terão que esperar até você voltar para casa.</li>
                                                                                <li>Deixar o seu pet em um hotel pode não funcionar para todos – eles podem ser alimentados com ração e petiscos novos, por exemplo.  Similarmente, é necessário verificar se o seu pet recebeu todas as vacinas exigidas pelo hotel.</li>
                                                                                <li>O seu pet pode escapar quando ficar entediado ou ficar sozinho por longos períodos de tempo.</li>
                                                                                <li>O seu pet pode gostar da alteração da rotina e de passar mais tempo com você durante as férias.</li>
                                                                                <li>Existem casas para aluguel e hotéis que cuidam do seu pet e adorariam tê-los presente durante as suas férias.</li>
                                                                            </ul>
                                                                                
