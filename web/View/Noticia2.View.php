                                                                            <p>Em vários lugares do mundo, o mês de junho é o mês nacional da microchipagem.  O microchip é um pouco maior que um grão de arroz e demora somente alguns segundos para o seu veterinário implantar um microchip no seu cachorro.  A partir do momento que o seu pet for microchipado, os seus dados de contato são imediatamente cadastrados em um base de dados – se o seu cachorro sumir e for encontrado por um abrigo ou veterinário, ele poderá ser escaneado, identificado e reunido com você.</p>
                                                                            <p>O microchip dura durante a vida do seu pet e não pode ser removido com facilidade, como uma coleira.  Enquanto os seus dados de contato estiverem atualizdos, o seu pet poderá ser reunido com você, mesmo em casos em que ele estiver milhares de quilômetros de distância.</p>
                                                                            <p>Mas é realmente importante eu microchipar o meu pet? escuto você se perguntar.  Esse procedimento simples, seguro, rápido, relativamente barato e sem dor pode ser a diferença entre ver o seu pet novamente ou perdê-lo para sempre, se ele se perder ou ser roubado.</p>
                                                                            <p>Se o seu pet sumir, você precisará informar a base de dados do microchip e eles enviarão diversas alertas para os veterinários e abrigos locais.</p>
                                                                            <p>A maioria dos veterinários, criadores e abrigos oferecem um bom serviço de microchipagem e o custo pode variar de zero até algumas centenas de reais.  Você deve ter percebido que o assunto de microchipagem está cada vez mais na mídia especialmente considerando que já é mandatório em alguns lugares.</p>
                                                                            <p>Eu acredito que a microchipagem seja essencial e é um parte importante de ser um dono responsável.  Ao garantir que os pets podem ser reunidos rapidamente com os seus donos, podemos ajudar a reduzir o número de gatos e cães abandonados em abrigos, além de garantir que filhotes são facilmente traçados aos seus criadores.</p>
										
										
                                                                                
                                                                                
