                                                                            <p>As fobias são extremamente comuns nas pessoas – mas os nossos animais também podem sofrer com medos irracionais.</p>
                                                                            <p>Várias das coisas que apavoram as pessoas e os animais são o resultado de reações acumuladas durante milhões de anos de evolução.  Por exemplo, temos medo de barulhos altos e alturas por que eles podem nos machucar, significando que os nossos instintos enraizados de ‘fuga ou luta’ começam a tomar conta.</p>
                                                                            <p>Porém, é possível superar ou pelo menos gerenciar diversos medos – tantos os racionais quanto os irracionais.  A Petplan analisou algumas fobias em gatos e cachorros e ofereceu conselhos sobre como ajudar o seu pet a lidar com os seus problemas.</p>
                                                                            <h4>Como posso ajudar o meu cachorro superar o medo de viajar de carro?</h4>
                                                                            <p>Existem vários cachorros que adoram viajar de carro mas, infelizmente, para alguns o passeio é muito mais um problema do que uma diversão.</p>
                                                                            <p>Vários motivos explicam porque o seu cachorro pode não gostar de andar de carro – coisas como ter sido envolvido em um acidente ou somente ter entrado no carro para ir até o veterinário podem contribuir para esse medo.</p>
                                                                            <p>A melhor maneira de ajudar o seu cachorro a gostar de passear de carro é acostumá-lo a ficar dentro do carro desde cedo.  Ande com ele de carro durante o dia e associe entrar no carro com coisas positivas e divertidas.</p>
                                                                            <p>Porém, se você tem um cachorro mais velho, esses medos podem já estar enraizados.  Nesse caso, existem algumas coisas que você pode fazer para ajudar o seu cão.  Acostumar o seu cachorro com um carro estacionado é um bom começo. Depois, quando ele estiver feliz, comece a fazer passeios curtos no carro, gradualmente aumentando a distância do passeio.</p>
                                                                            <p>Recompense o seu pet pelo bom comportamento e, se ele ficar estressado, tire-o do carro — o bem estar do seu cachorro é obviamente a coisa mais importante.</p>
                                                                            <h4>Como posso ajudar o meu gato ter menos medo de água?</h4>
                                                                            <p>Enquanto vários gatos – particularmente os gatos que vivem em climas quentes – gostam de se refrescar na água, ainda pode ser um grande problema para vários donos manter os seus gatos limpos com água.</p>
                                                                            <p>Vale a pena lembrar que os gatos gastam horas se limpando então os banhos só são necessário se os gatos estiverem muito sujos. Mas, se você precisa dar banho no seu gato, abaixo estão algumas dicas para te ajudar.</p>
                                                                            <p>O ideal é apresentar o seu gato ao banho desde filhote para ele se acostumar com a água.  Gradualmente apresente-o a água, lave-o com uma toalha umedecida e pouco a pouco apresente mais água ao banho.</p>
                                                                            <p>Você também deve tomar cuidado na hora de utilizar gel e shampoo, dado que vários gatos são super sensíveis a diferentes tipos de produtos químicos e isso pode contribuir para eles não gostarem de tomar banho.</p>
                                                                            <h4>Como posso ajudar o meu cachorro a superar o seu medo de outros cães?</h4>
                                                                            <p>Algumas fobias podem ser evitadas completamente, mas se o seu cachorro tiver medo de outros cães a sua qualidade de vida pode severamente afetada, fazendo desse um problema que precisa ser tratado.</p>
                                                                            <p>Similar aos outros medos, medo de outros cachorros provavelmente foi adquirido durante a infância de um cão por conta de alguma experiência traumática ou falta de interação com outros cães enquanto filhote.</p>
                                                                            <p>Infelizmente, medo de outros cães ou animais pode se manifestar como agressividade e por isso você deve trabalhar devagar na hora de integrar o seu pet com outros cães.  Sempre mantenha o seu cachorro na coleira e mantenha uma distância segura entre ele e outros cães.</p>
                                                                            <p>Se o seu cachorro não fica agressivo quando está com medo, garanta que você esteja relaxado e confiante quando estiver com outros animais.  Mantenha a sua voz calma e sempre esteja alerta para outros cachorros não chegarem muito perto e assustar o seu pet.</p>
                                                                            <p>Esse problema é extremamente complicado e, com relação ao seu tratamento, existem diversas opções a serem consideradas.  Um treinador profissional ou veterinário poderá te dar bons conselhos e te ajudar a escolher a melhor opção: para você e o seu cachorro.</p>
                                                                            <h4>Como posso ajudar o meu cachorro ter menos medo de barulhos altos?</h4>
                                                                            <p>Conforme mencionado previamente, as pessoas e os animais comumente sentem medo das coisas para o seu próprio bem – barulhos altos é uma dessas coisas e pode ser algo difícil de curar completamente.</p>
                                                                            <p>Porém, existem algumas coisas que você pode fazer que facilitará os medos do seu cachorro durante períodos em que barulhos altos são inevitáveis, como na noite de um jogo de futebol.</p>
                                                                            <p>Você deve criar um lugar seguro e confortável para o seu cachorro se esconder durante esses momentos.  O lugar deve ser aconchegante e escuro e, se possível, ter coisas como um rádio ou televisão por perto para o barulho desses aparelhos distraí-lo.</p>
                                                                            <p>A distração é outra ferramenta que você deve utilizar.  Alimente o seu cachorro quando esses barulhos altos estiverem ocorrendo ou  brinque com ele para mantê-lo engajado e reduzir a probabilidade de ele focar no barulho.</p>
                                                                            <p>Como sempre, essas são somente dicas e, se o seu gato ou cachorro estiver sofrendo com fobias ou medos que estão difíceis de combater, consulte o seu veterinário que poderá te dar conselhos específicos para a sua situação.</p>
                                                                                
                                                                                
